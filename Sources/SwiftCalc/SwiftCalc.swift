final class SwiftCalc {
    public func add(this numA: Int, with numB: Int) -> Int {
        numA + numB
    }

    public func subtract(this numA: Int, with numB: Int) -> Int {
        numA - numB
    }

    public func divide(this numA: Int, with numB: Int) -> Int {
        numA / numB
    }

    public func multiply(this numA: Int, with numB: Int) -> Int {
        numA * numB
    }
}
